def format_string(input_text):
    # Split the input string by newlines to get each individual entry
    entries = input_text.splitlines()
    
    # Remove duplicates using a set and filter out any empty lines
    unique_entries = set(entry.strip() for entry in entries if entry.strip())
    
    # Join the unique entries with a comma and space
    formatted_string = ", ".join(unique_entries)
    
    return formatted_string

